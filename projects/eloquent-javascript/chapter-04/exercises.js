function range(a, b, c) {
 let count = [];
 if(a < b && c > 0) {
     for(let i = a; i <= b; i +=c) {
         count.push(i);
     } return count;
 } else if (a > b && c > 0) {
     for(let i = b; i <= a; i += c) {
        count.push(i);
     } return count;
 } else if(a === b || c < 0){
     return count;
 } else if (a < b /*&& c === null*/) {
     for(let i = a; i <= b; i ++) {
         count.push(i);
     } return count;
 } else if (a > b /*&& c === null*/) {
     for(let i = b; i <= a; i ++) {
         count.push(i);
     } return count;
 } 
}

