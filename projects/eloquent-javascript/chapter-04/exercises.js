////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list) {
let arr = [];
while(list){
    arr.push(list.value)
    list = list.rest;
}
return arr;
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(item, list) {
 var node = listToArray(list);
  node.unshift(item);
  var node = arrayToList(node);
  return node;
}

